#include <stdio.h>
//CH.SC.U4CSE24050
typedef unsigned int UI;// Define a new type UI as an alias for unsigned int

int main() {
    UI a = 10;// Declare a variable 'a' of type UI and initialize it to 10
    // The variable 'a' is now an unsigned integer with a value of 10
    printf("%u\n", a);
    return 0;
}
